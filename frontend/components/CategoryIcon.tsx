type Props = {
  emoji?: string;
  themeId?: string;
  size: number;
  color: string;
  type?: 'super' | 'cluster' | 'theme';
};

export default function CategoryIcon({ emoji, themeId, size, color, type = 'super' }: Props) {
  // Theme ID lookup first
  if (themeId && THEME_ICON_MAP[themeId]) {
    const icon = THEME_ICON_MAP[themeId];
    return renderIcon(icon, size, color);
  }

  // Emoji-based lookup — try primary map first, then fallback to all maps
  if (emoji) {
    const primaryMap = type === 'cluster' ? CLUSTER_ICON_MAP : SUPER_ICON_MAP;
    const icon = primaryMap[emoji] || SUPER_ICON_MAP[emoji] || CLUSTER_ICON_MAP[emoji];
    if (icon) return renderIcon(icon, size, color);
    return <Text style={{ fontSize: size * 0.8 }}>{emoji}</Text>;
  }

  // Fallback générique
  return <MaterialCommunityIcons name="help-circle-outline" size={size} color={color} />;
}

function renderIcon(icon: IconDef, size: number, color: string) {
  switch (icon.set) {
    case 'mci':
      return <MaterialCommunityIcons name={icon.name as any} size={size} color={color} />;
    case 'ion':
      return <Ionicons name={icon.name as any} size={size} color={color} />;
    case 'fa5':
      return <FontAwesome5 name={icon.name} size={size} color={color} />;
  }
}
